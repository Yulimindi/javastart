package variable;

public class Var8 {
    public static void main(String[] args) {

        // 정수
        byte b = 127; // -128~127
        short s = 32767; // -32768~32767
        int i = 2147483647; // -2147483648~ 2147483647
        long l = 9223372036854775807L;

        // 실수
        float f = 10.0f;
        double d = 10.0;

        // 클래스 선언 시 첫글자 대문자 : Person, OrderDetail
        // 변수를 포함한 나머지는 낙타 표기법 : firstName, userAccount
        // 상수는 대문자 사용 후 언더바로 구분 : USER_LIMIT
        // 패키지는 전부 소문자 사용 : org.spring.boot
    }
}
